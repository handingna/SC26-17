function dominantElement(chart: BirthChart): ElementType {
  let max = -1;
  let dominant: ElementType = "木";
  for (const priority of ELEMENT_PRIORITY) {
    const entry = chart.elements.find((e) => e.element === priority);
    if (entry && entry.count > max) {
      max = entry.count;
      dominant = priority;
    }
  }
  return dominant;
}

export function getFortuneSlip(chart: BirthChart, date: string): FortuneSlip {
  const element = dominantElement(chart);
  const dayOfMonth = parseInt(date.slice(8, 10), 10);
  const index = dayOfMonth % 7;
  const [title, body, intention] = SLIPS[element][index];
  return { element, index, title, body, intention };
}
